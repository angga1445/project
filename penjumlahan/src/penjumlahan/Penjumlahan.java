public class Penjumlahan {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
       
        int a=8;
        int b=5;
        int tambah,bagi,kali,kurang;
        tambah= a+b;
        kurang=a-b;
        kali=a*b;
        bagi=a/b;
        System.out.println("\n"+"nilai penjumlahan: "+tambah+"\n"+"Nilai pengurangan:"+kurang+"\n"+"Nilai perkalian:"+kali+"\n"+"Nilai pembagian:"+bagi);
    }
    
}
